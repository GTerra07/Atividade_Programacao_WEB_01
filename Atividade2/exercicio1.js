// Declaração das variáveis numéricas
let numA = 10;
let numB = 5;

// Operações matemáticas
let soma = numA + numB;
let subtracao = numA - numB;
let multiplicacao = numA * numB;
let divisao = numA / numB;
let resto = numA % numB;

// Impressão dos resultados
console.log("Resultado da soma entre", numA, "e", numB, "=", soma);
console.log("Resultado da subtração entre", numA, "e", numB, "=", subtracao);
console.log("Resultado da multiplicação entre", numA, "e", numB, "=", multiplicacao);
console.log("Resultado da divisão entre", numA, "e", numB, "=", divisao);
console.log("Resultado do resto da divisão entre", numA, "e", numB, "=", resto);